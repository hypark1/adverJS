                        <p>서울 동대문구 회기동에 위치한 경희대학교 서울캠퍼스. 벚꽃 캠퍼스뿐 아니라 벚꽃 명소로도 유명한 곳이다. 경희대 벚꽃 명당은 본관 앞 분수대다. 분수대 앞에 앉으면, 벚꽃으로 둘러싸인 경희대 전경이 한눈에 보인다. 인생샷 장소로도 최고다.</p>
                        <figure class="image">
                            <img src="http://img1.medicalreport.kr/site4/201903/l/5c8f59019f11820190318173825651524.jpeg">
                            <figcaption>(출처=GettyImagesBank)</figcaption>
                        </figure>
                        <p>서울 용산구 청파동에 위치한 숙명여자대학교. 숙명여자대학교 인생샷 명당은 명신관 오르막길과 중앙도서관 가는 길이다. 벚꽃이 만개했을 때 인생샷을 찍어보자. 캠퍼스 벚꽃 구경만으로는 아쉽다면 근처 효창공원에 방문해보는 것도 좋다.</p>
                        <figure class="image">
                            <img src="http://img1.medicalreport.kr/site4/201903/l/5c8f5901b2eec20190318173825732888.jpeg">
                            <figcaption>(출처=인하대학교)</figcaption>
                        </figure>
                        <p>인천광역시 용현동에 위치한 인하대학교. 인하대학생들은 물론 인천 시민들까지 사진 찍으러 오는 벚꽃 명소다. 인하대학교 벚꽃 명당은 학생회관 근처에 있는 호수 ‘인경호’ 앞이다. 호숫가를 걸으며 벚꽃을 만끽해보자.</p>

