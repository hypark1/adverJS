                        <p>‘벚꽃’의 계절 봄이 왔다. 대학가에서 벚꽃은 ‘커플 탄생’을 의미한다. 새 학기가 시작되고 벚꽃이 피면, 캠퍼스커플 일명 'CC'가 대거 탄생한다.</p>
                        <p>썸남 썸녀가 함께 걸으면 커플 되는 벚꽃 대학교 캠퍼스를 소개한다.</p>
